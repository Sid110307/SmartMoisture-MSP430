void oledClear(void)
{
	oledSetCursor(0, 0);
	for (uint16_t i = 0; i < 1024; ++i) oledSendData(0x00);
}

void oledDrawString(const uint8_t col, const uint8_t page, const char* s)
{
	uint8_t x = col;
	while (*s && x < 128)
	{
		oledDrawChar(x, page, *s++);
		x += 6;
	}
}
